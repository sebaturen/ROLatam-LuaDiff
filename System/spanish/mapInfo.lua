function main()
  for name, info in pairs(mapTbl) do
    displayName = "Zona desconocida"
    if nil ~= info.displayName then
      displayName = info.displayName
    end
    notify_enter = false
    if nil ~= info.notifyEnter then
      notify_enter = info.notifyEnter
    end
    result, msg = AddMapDisplayName(name, displayName, notify_enter)
    if nil ~= info.signName then
      subTitle = ""
      if nil ~= info.signName.subTitle then
        subTitle = info.signName.subTitle
      end
      mainTitle = ""
      if nil ~= info.signName.mainTitle then
        mainTitle = info.signName.mainTitle
      end
      result, msg = AddMapSignName(name, subTitle, mainTitle)
    end
    if nil ~= info.backgroundBmp then
      result, msg = AddMapBackgroundBmp(name, info.backgroundBmp)
    end
  end
  return result, msg
end
