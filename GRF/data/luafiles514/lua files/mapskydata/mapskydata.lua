function IsExistMapSkyData(mapName)
  return nil ~= MapSkyData[mapName]
end
function GetBackgroundColor(mapName)
  if nil == MapSkyData[mapName].BG_Color then
    return 0, 0, 0
  end
  return MapSkyData[mapName].BG_Color[1], MapSkyData[mapName].BG_Color[2], MapSkyData[mapName].BG_Color[3]
end
function IsDrawStar(mapName)
  if nil == MapSkyData[mapName].Star_Effect then
    return false
  end
  return MapSkyData[mapName].Star_Effect
end
function GetOldCloudTypeNum(mapName)
  if nil == MapSkyData[mapName].Old_Cloud_Effect then
    return 0
  end
  return #MapSkyData[mapName].Old_Cloud_Effect
end
function GetOldCloudInfo(mapName, id)
  if nil == MapSkyData[mapName].Old_Cloud_Effect or nil == MapSkyData[mapName].Old_Cloud_Effect[id] then
    return -1
  end
  return MapSkyData[mapName].Old_Cloud_Effect[id]
end
function GetCloudTypeNum(mapName)
  if nil == MapSkyData[mapName].Cloud_Effect then
    return 0
  end
  return #MapSkyData[mapName].Cloud_Effect
end
function GetCloudInfo(mapName, id)
  local CloudEffect = MapSkyData[mapName].Cloud_Effect[id]
  if nil == CloudEffect then
    return nil
  end
  return CloudEffect.Num, CloudEffect.CullDist, CloudEffect.Color[1], CloudEffect.Color[2], CloudEffect.Color[3], CloudEffect.Size, CloudEffect.Size_Extra, CloudEffect.Expand_Rate, CloudEffect.Alpha_Inc_Time, CloudEffect.Alpha_Dec_Time_Extra, CloudEffect.Alpha_Inc_Speed, CloudEffect.Alpha_Dec_Time, CloudEffect.Alpha_Dec_Time_Extra, CloudEffect.Alpha_Dec_Speed, CloudEffect.Height, CloudEffect.Height_Extra
end
function IsBackGroundFog(mapName)
  if nil == MapSkyData[mapName].BG_Fog then
    return false
  end
  return MapSkyData[mapName].BG_Fog
end
